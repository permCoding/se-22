// Две точки для вызова метода в 123456..toString(36) - почему ?
// Если требуется вызвать метод непосредственно на числе, то надо поставить две точки .. после числа
// Если поставим одну точку: 123456.toString(36), тогда это будет ошибкой, поскольку синтаксис JS предполагает, 
// что после первой точки начинается десятичная часть. 
// А если поставить две точки, то JS понимает, что десятичная часть отсутствует, и начинается метод.
// Также можно записать как (123456).toString(36).

let a = 0b11111111; // бинарная форма записи числа 255
let b = 0o377; // восьмеричная форма записи числа 255
let c = 0xff;
let d = 255;

console.log(a === b); // true, с двух сторон число 255
console.log(c === d); // true, с двух сторон число 255
